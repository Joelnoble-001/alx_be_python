# hours to seconds calc

#define the variables
hours = 2  # representing 2 hours

# calculate seconds
seconds = hours * 3600  # 1 hour = 3600seconds(since there are 60 minutes in an hour and 60 seconds in a minute, thus 60 x 60 = 3600).

print(str(hours) + " hour (s) is equal to " + str(seconds) + " seconds.")
