# Basic Operations 

number1 = 10
number2 = 5 

# Perform arithmetic operations

addition = number1 + number2 # this will add the two numbers 
subtraction = number1 - number2 # this will subtract the second number from the first
multiplication = number1 * number2 # this will multiply the two numbers

#print the results

print("Addition of " + str(number1) + " and " + str(number2) + " is " + str(addition))
print("Subtraction of " + str(number1) + " and " + str(number2) + " is " + str(subtraction))
print("Multiplication of " + str(number1) + " and " + str(number2) + " is " + str(multiplication))
