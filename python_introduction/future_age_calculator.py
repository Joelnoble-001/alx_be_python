# future age calculator

# prompt the user for their current age
current_age = int(input("How old are you? "))

# calculate the age in 2050 (27years after 2023(assuming the current year is 2023))
future_age_2050 = current_age + 27

# print the result
print("In 2050, you will be " + str(future_age_2050) + " years old.")